    public ClientesRegistrados() {
        initComponents();
        
        DefaultTableModel modeloTabla= (DefaultTableModel) tblClientesRegistrados.getModel();
        List<Afiliado>listaCliente = consultaAll();
        for (Afiliado afiliado : listaCliente) {
            
            Vector vecAfiliado = new Vector();
            vecAfiliado.add(afiliado.getDocumento());
            vecAfiliado.add(afiliado.getNombre()+" "+afiliado.getApellido());
            vecAfiliado.add(afiliado.getCiudad());
            
            List<Poliza>listaPoliza=polizaBuscar(afiliado.getIdAfiliado());
            for (Poliza poliza : listaPoliza) {
                vecAfiliado.add(poliza.getTipo());
                vecAfiliado.add(poliza.getValor());
            }
            
            List<Siniestro>listaSiniestro = siniestroBuscar(afiliado.getIdAfiliado());
            if (listaSiniestro.size() > 0) {
                vecAfiliado.add("SI");
            }else{
                vecAfiliado.add("No");
            }
            modeloTabla.addRow(vecAfiliado);
        }
    }

