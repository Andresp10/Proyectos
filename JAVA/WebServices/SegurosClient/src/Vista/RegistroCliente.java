    private static int cliente(java.lang.String accion, java.lang.String documento, java.lang.String nombre, java.lang.String apellido, java.lang.String fechaNacimiento, java.lang.String genero, java.lang.String departamento, java.lang.String ciudad, java.lang.String direccion, java.lang.String estadoCivil, java.lang.String estado) {
        webservices.WSSeguros_Service service = new webservices.WSSeguros_Service();
        webservices.WSSeguros port = service.getWSSegurosPort();
        return port.cliente(accion, documento, nombre, apellido, fechaNacimiento, genero, departamento, ciudad, direccion, estadoCivil, estado);
    }

    private static int clienteBuscar(java.lang.String documento) {
        webservices.WSSeguros_Service service = new webservices.WSSeguros_Service();
        webservices.WSSeguros port = service.getWSSegurosPort();
        return port.clienteBuscar(documento);
    }

    private static int polizaCreate(java.lang.String tipo, double valor, int afiliado) {
        webservices.WSSeguros_Service service = new webservices.WSSeguros_Service();
        webservices.WSSeguros port = service.getWSSegurosPort();
        return port.polizaCreate(tipo, valor, afiliado);
    }

    
