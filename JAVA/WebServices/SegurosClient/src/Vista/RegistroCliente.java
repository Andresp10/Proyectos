    private void btnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarActionPerformed
        // TODO add your handling code here:
        System.out.println(Double.parseDouble(String.valueOf(cmbValorPoliza.getSelectedItem())));
        String Documento="",Nombre="",Apellido="",FechaNacimiento="",Genero="",Departamento="",Ciudad="",Direccion="",EstadoCivil="",Estado="Activo";
        
        Documento = txtDocumento.getText();
        Nombre = txtNombre.getText();
        Apellido = txtApellido.getText();
        FechaNacimiento = txtFechaNacimiento.getText();
        Genero = String.valueOf(cmbGenero.getSelectedItem());
        Departamento = txtDepartamento.getText();
        Ciudad = txtCiudad.getText();
        Direccion = txtDireccion.getText();
        EstadoCivil = String.valueOf(cmbEstadoCivil.getSelectedItem());
        
        int result = cliente("Crear", Documento, Nombre, Apellido, FechaNacimiento, Genero, Departamento, Ciudad, Direccion, EstadoCivil, Estado);
        if (result == 201) {
            int idCliente = clienteBuscar(Documento);
            int resultPoliza = polizaCreate(String.valueOf(cmbTipoPoliza.getSelectedItem()), Double.parseDouble(String.valueOf(cmbValorPoliza.getSelectedItem())), idCliente);
            if (resultPoliza == 201) {
                JOptionPane.showMessageDialog(this, "Cliente registrado exitosamente.");
                new ClientesRegistrados().setVisible(true);
                this.setVisible(false);
            }
            
        }else{
        
            JOptionPane.showMessageDialog(this, "Ocurrió un error en el registro, por favor intentalo mas tarde.");
        }
        
    }//GEN-LAST:event_btnFinalizarActionPerformed

