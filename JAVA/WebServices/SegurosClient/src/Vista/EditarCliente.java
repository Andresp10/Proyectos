    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        
        String documentoAfiliado[] = String.valueOf(cmbCliente.getSelectedItem()).split("-");
        int idAfiliado = clienteBuscar(documentoAfiliado[0]);
        int resultAfiliado = cliente("Editar/"+documentoAfiliado[0]+"", txtDocumento.getText(), txtNombre.getText(), txtApellido.getText(), txtFechaNacimiento.getText(), String.valueOf(cmbGenero.getSelectedItem()), txtDepartamento.getText(), txtCiudad.getText(), txtDireccion.getText(), String.valueOf(cmbEstadoCivil.getSelectedItem()), "Activo");
        if (resultAfiliado == 200) {
            
            
            int resultEditarPoliza = editarPoliza(String.valueOf(cmbTipoPoliza.getSelectedItem()), Double.parseDouble(String.valueOf(cmbValorPoliza.getSelectedItem())), idAfiliado);
            if (resultEditarPoliza == 200) {
               
                if (txtFechaSiniestro.getText() != "") {
                    
                    Afiliado objAfiliado = new Afiliado();
                    objAfiliado.setIdAfiliado(idAfiliado);
                    Siniestro objSiniestro = new Siniestro();
                    objSiniestro.setAfiliado(objAfiliado);
                    objSiniestro.setTipo(String.valueOf(cmbTipoSiniestro.getSelectedItem()));
                    int resultadoEditarSiniestro = editarSiniestro(objSiniestro);
                    if (resultadoEditarSiniestro == 200) {
                        JOptionPane.showMessageDialog(this, "Datos editados exitosamente.");
                        new ClientesRegistrados().setVisible(true);
                        this.setVisible(false);
                        
                    }else{
                        JOptionPane.showMessageDialog(this, "Error el editar los datos del Cliente, por favor verifique sus datos e intentelo de nuevo");
                    }
                }else{
                    JOptionPane.showMessageDialog(this, "Datos editados exitosamente.");
                }
            }
            
        }else{
            
            JOptionPane.showMessageDialog(this, "Error el editar los datos del Cliente, por favor verifique sus datos e intentelo de nuevo");
        }
        
    }//GEN-LAST:event_btnEditarActionPerformed

