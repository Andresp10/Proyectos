    private void cmbClienteItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbClienteItemStateChanged
        // TODO add your handling code here:
        List<Afiliado>listafiliado = buscarDatosCliente(cmbCliente.getSelectedIndex());
        for (Afiliado afiliado : listafiliado) {
            String fecha[] = String.valueOf(afiliado.getFechaNacimiento()).split("T");
            txtDocumento.setText(afiliado.getDocumento());
            txtNombre.setText(afiliado.getNombre());
            txtApellido.setText(afiliado.getApellido());
            txtFechaNacimiento.setText(fecha[0]);
            txtDepartamento.setText(afiliado.getDepartamento());
            txtCiudad.setText(afiliado.getCiudad());
            txtDireccion.setText(afiliado.getDireccion());
            if (afiliado.getGenero().equals("Masculino")) {
                cmbGenero.setSelectedIndex(1);
            }else if (afiliado.getGenero().equals("Femenino")) {
                cmbGenero.setSelectedIndex(2);
            }else{
                cmbGenero.setSelectedIndex(3);
            }
            
            if (afiliado.getEstadoCivil().equals("Soltero")) {
                cmbEstadoCivil.setSelectedIndex(1);
            }else if (afiliado.getEstadoCivil().equals("Casado")) {
                cmbEstadoCivil.setSelectedIndex(2);
            }else if (afiliado.getEstadoCivil().equals("Divorciado")) {
                cmbEstadoCivil.setSelectedIndex(3);
            }else{
                cmbEstadoCivil.setSelectedIndex(4);
            }
            
            List<Poliza>listaPoliza = polizaBuscar(afiliado.getIdAfiliado());
            for (Poliza poliza : listaPoliza) {
                if (poliza.getTipo().equals("SOAT")) {
                    cmbTipoPoliza.setSelectedIndex(1);
                }else{
                    cmbTipoPoliza.setSelectedIndex(2);
                }
                if (poliza.getValor() == 68000) {
                    cmbValorPoliza.setSelectedIndex(1);
                }else{
                    cmbValorPoliza.setSelectedIndex(2);
                }
            }
            
            List<Siniestro>listasiniestro = siniestroBuscar(afiliado.getIdAfiliado());
            if (listasiniestro.size() > 0) {
                txtFechaSiniestro.setEnabled(true);
                cmbTipoSiniestro.setEnabled(true);
                for (Siniestro siniestro : listasiniestro) {
                    String fechaSiniestro[] = String.valueOf(siniestro.getFecha()).split("T");
                    txtFechaSiniestro.setText(fechaSiniestro[0]);
                    if (siniestro.getTipo().equals("Natural")) {
                        cmbTipoSiniestro.setSelectedIndex(1);
                    }else if (siniestro.getTipo().equals("Suicidio")) {
                        cmbTipoSiniestro.setSelectedIndex(2);
                    }else{
                        cmbTipoSiniestro.setSelectedIndex(3);
                    }
                }
            }else{
                txtFechaSiniestro.setEnabled(false);
                cmbTipoSiniestro.setEnabled(false);
                txtFechaSiniestro.setText("");
                cmbTipoSiniestro.setSelectedIndex(0);
            }
            
        }
    }//GEN-LAST:event_cmbClienteItemStateChanged

