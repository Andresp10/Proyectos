    private void btnRegistrarSiniestroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarSiniestroActionPerformed
        // TODO add your handling code here:
        String Afiliado = String.valueOf(cmbCliente.getSelectedItem());
        String documentoAfiliado[] = Afiliado.split("-");
        int idAfiliado = clienteBuscar(documentoAfiliado[0]);
        int result = siniestroCreate(txtFecha.getText(), String.valueOf(cmbTipo.getSelectedItem()), idAfiliado);
        if (result == 201) {
            JOptionPane.showMessageDialog(this, "Siniestro registrado exitosamente.");
            new SiniestrosRegistrados().setVisible(true);
            this.setVisible(false);
        }else{
            JOptionPane.showMessageDialog(this, "Error al crear el registro, por favor intentalo mas tarde.");
        }
    }//GEN-LAST:event_btnRegistrarSiniestroActionPerformed

